#include "ofApp.h"	

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(30);
	ofSetWindowTitle("openFrameworks");

	ofBackground(239);
	ofEnableDepthTest();

	this->frame_mesh.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);

	ofFbo fbo;
	fbo.allocate(ofGetWidth(), ofGetHeight());
	fbo.begin();
	ofTranslate(ofGetWidth() * 0.5, ofGetHeight() * 0.5);
	ofClear(0);
	ofSetColor(0);

	ofTrueTypeFont font;
	font.loadFont("fonts/Kazesawa-Bold.ttf", 200, true, true, true);
	/* https://github.com/kazesawa/kazesawa */

	font.drawString("20", font.stringWidth("20") * -0.5, font.stringHeight("20") * -0.05);
	font.drawString("19", font.stringWidth("19") * -0.5, font.stringHeight("19") * 1.05);

	fbo.end();

	ofPixels pixels;
	fbo.readToPixels(pixels);
	auto span = 10;

	ofColor pix_color;
	for (int x = 0; x < fbo.getWidth(); x += span) {

		for (int y = 0; y < fbo.getHeight(); y += span) {

			pix_color = pixels.getColor(x, y);
			if (pix_color != ofColor(0, 0)) {

				for (int z = span * -2; z <= span * 2; z += span) {

					this->location_list.push_back(glm::vec3(x - ofGetWidth() * 0.5, y - ofGetHeight() * 0.5, z));
				}
			}
		}
	}

	for (auto& location : this->location_list) {

		vector<int> next_index = vector<int>();
		int index = -1;
		for (auto& other : this->location_list) {

			index++;
			if (location == other) { continue; }

			float distance = glm::distance(location, other);
			if (distance <= span) {

				next_index.push_back(index);
			}
		}

		this->next_index_list.push_back(next_index);
	}

	for (int i = 0; i < 1800; i++) {

		this->actors.push_back(new Actor(this->location_list, this->next_index_list, this->destination_list));
	}
}


//--------------------------------------------------------------
void ofApp::update() {

	int frame_span = 15;
	int prev_index_size = 0;

	if (ofGetFrameNum() % frame_span == 0) {

		prev_index_size = this->destination_list.size();
	}

	for (auto actor : this->actors) {

		actor->update(frame_span, this->location_list, this->next_index_list, this->destination_list);
	}

	if (prev_index_size != 0) {

		this->destination_list.erase(this->destination_list.begin(), this->destination_list.begin() + prev_index_size);
	}

	this->face_mesh.clear();
	this->frame_mesh.clear();
	for (auto actor : this->actors) {
		
		this->setBoxToMesh(this->face_mesh, this->frame_mesh, actor->getLocation(), 10);
	}

	for (auto& vertex : this->face_mesh.getVertices()) {

		auto value = ofMap(ofNoise(vertex.x * 0.008, vertex.y * 0.008, vertex.z * 0.008, ofGetFrameNum() * 0.01), 0, 1, 0, 255);
		this->face_mesh.addColor(ofColor(value));
		this->frame_mesh.addColor(ofColor(255 - value));
	}

}

//--------------------------------------------------------------
void ofApp::draw() {

	this->cam.begin();
	ofRotateX(180);
	ofRotateY(ofGetFrameNum() * 2);

	this->face_mesh.drawFaces();
	this->frame_mesh.drawWireframe();

	this->cam.end();
}

//--------------------------------------------------------------
void ofApp::setBoxToMesh(ofMesh& face_target, ofMesh& frame_target, glm::vec3 location, float size) {

	this->setBoxToMesh(face_target, frame_target, location, size, size, size);
}

